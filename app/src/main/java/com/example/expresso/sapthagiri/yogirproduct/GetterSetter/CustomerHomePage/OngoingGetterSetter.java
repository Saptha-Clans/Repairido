package com.example.expresso.sapthagiri.yogirproduct.GetterSetter.CustomerHomePage;

public class OngoingGetterSetter {
    String ongoing_title;
    String ongoing_description;

    public OngoingGetterSetter(String ongoing_title, String ongoing_description) {
        this.ongoing_title = ongoing_title;
        this.ongoing_description = ongoing_description;
    }

    public String getOngoing_title() {
        return ongoing_title;
    }

    public void setOngoing_title(String ongoing_title) {
        this.ongoing_title = ongoing_title;
    }

    public String getOngoing_description() {
        return ongoing_description;
    }

    public void setOngoing_description(String ongoing_description) {
        this.ongoing_description = ongoing_description;
    }

}
